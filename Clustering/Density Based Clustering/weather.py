pdf = pd.read_csv("https://raw.githubusercontent.com/sumyak/ML-Algos-and-Techniques/master/Clustering/Density%20Based%20Clustering/weather-stations20140101-20141231.csv")
pdf = pdf[pd.notnull(pdf["Tm"])]
